#  Add / Insert Methods---

# Add-insert method in python ----

name = ["manish" , "tiwari"]
name.append("anaya")
print(name)


# insert(index, item)---
name.insert(1 , "mango")
print(name)


# Search / Count Methods---

color = ['red', 'green', 'green', 'blue', 'purple', 'pink', 'brown']

print(color.index("green"))
print(color.count("green"))


#  Sorting / Reverse Methods----
student = ["manish","pankaj" , "rohit" , "himanshu" , "akash" , "aryan" ,"hariom","sora","anamika"]
student.sort()
print(student)

number = [3,6,7,4,3,7,8,5,9,78,9,4]
